/** Render a social link as a clickable icon + username string (for use in React JSX via data return) */
export function getSocialMeta(link: SocialLink) {
  const meta = SOCIAL_PLATFORMS[link.platform];
  return {
    label: meta.label,
    color: meta.color,
    url: getSocialUrl(link),
    icon: SOCIAL_ICONS[link.platform],
    username: link.username,
  };
}
