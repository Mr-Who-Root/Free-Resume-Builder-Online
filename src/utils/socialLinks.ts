/** Resolve the final URL for a social link */
export function getSocialUrl(link: SocialLink): string {
  if (link.url) return link.url;
  const meta = SOCIAL_PLATFORMS[link.platform];
  if (!meta.baseUrl) return link.username; // 'other' — treat username as the URL
  return `${meta.baseUrl}${link.username}`;
}

